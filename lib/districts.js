// Helper to get district by name
export function getDistrictByName(name) {
    return bangladeshDistricts.find(d => d.name === name.toLowerCase())
}

// Helper to convert to coordinates object (for backward compatibility)
export const districtCoordinates = bangladeshDistricts.reduce((acc, district) => {
    acc[district.name] = {
        lat: district.lat,
        lng: district.lng,
        bengali: district.bengali,
        division: district.division
    }
    return acc
}, {})
